//--------------------------------------------------------------------------------------------------------------------------------
// @bried 初期化
//--------------------------------------------------------------------------------------------------------------------------------
void Game::Initialize()
{
}

//--------------------------------------------------------------------------------------------------------------------------------
// @brief ゲーム更新処理
//--------------------------------------------------------------------------------------------------------------------------------
void Game::Update()
{
    // Mキーが押されたら
    if (CheckHitKey(KEY_INPUT_M))
    {
        // シーンをメニューに変更
        SceneManager::ChangeScene(SceneManager::Scene_Menu);
    }
}

//--------------------------------------------------------------------------------------------------------------------------------
// @brief ゲーム描画処理
//--------------------------------------------------------------------------------------------------------------------------------
void Game::Draw()
{
    DrawString(0, 0, "ゲーム画面です。", GetColor(255, 255, 255));
    DrawString(0, 0, "Mキーを押すとメニュー画面に戻ります。", GetColor(255, 255, 255));
}

