#include "Player.h"
#include "DxLib.h"

//--------------------------------------------------------------------------------------------------------------------------------
// @brief コンストラクタ
//--------------------------------------------------------------------------------------------------------------------------------
Player::Player()
{
    posX = 100;
    posY = 100;
    image = LoadGraph("Assets/Player/test.png");
}

//--------------------------------------------------------------------------------------------------------------------------------
// @brief デストラクタ
//--------------------------------------------------------------------------------------------------------------------------------
Player::~Player()
{
}

//--------------------------------------------------------------------------------------------------------------------------------
// @brief プレイヤー更新処理
//--------------------------------------------------------------------------------------------------------------------------------
void Player::Update()
{
    Move();
}

//--------------------------------------------------------------------------------------------------------------------------------
// @brief プレイヤー描画処理
//--------------------------------------------------------------------------------------------------------------------------------
void Player::Draw()
{
    DrawRotaGraph((int)posX, (int)posY, scaling, 0, image, FALSE);
}

//--------------------------------------------------------------------------------------------------------------------------------
// @brief プレイヤー移動処理
//--------------------------------------------------------------------------------------------------------------------------------
void Player::Move()
{
    if (CheckHitKey(KEY_INPUT_LEFT))
    {
        posX--;
    }

    if (CheckHitKey(KEY_INPUT_RIGHT))
    {
        posX++;
    }
}
