#include "Menu.h"
#include "DxLib.h"
#include "SceneManager.h"
#include "SoundManager.h"

//--------------------------------------------------------------------------------------------------------------------------------
// @brief コンストラクタ
//--------------------------------------------------------------------------------------------------------------------------------
Menu::Menu()
{
}

//--------------------------------------------------------------------------------------------------------------------------------
// @bried デストラクタ
//--------------------------------------------------------------------------------------------------------------------------------
Menu::~Menu()
{
}

//--------------------------------------------------------------------------------------------------------------------------------
// @brief メニュー更新処理
//--------------------------------------------------------------------------------------------------------------------------------
void Menu::Update()
{
    SoundManager::StartSound(0);

    // Gキーが押されたら
    if (CheckHitKey(KEY_INPUT_G))
    {
        SoundManager::StopAll();
        // シーンをゲーム画面に変更
        SceneManager::ChangeScene(SceneManager::SceneState::Scene_Game);
    }

    // Cキーが押されたら
    if (CheckHitKey(KEY_INPUT_C))
    {
        // シーンを設定画面に変更
        SceneManager::ChangeScene(SceneManager::SceneState::Scene_Config);
    }
}

//--------------------------------------------------------------------------------------------------------------------------------
// @brief メニュー描画処理
//--------------------------------------------------------------------------------------------------------------------------------
void Menu::Draw()
{
    int fontSize = 60;
    int white = GetColor(255, 255, 255);
    DrawString(600, fontSize * 7, "メニュー画面です。", white);
    DrawString(600, fontSize * 8, "Gキーを押すとゲーム画面に進みます。", white);
}
